public class VideoFileModel {
    private String frontVideoPath;
    private String leftVideoPath;
    private String bottomVideoPath;
    private String rightVideoPath;
    private String topVideoPath;

    public String getFrontVideoPath() {
        return frontVideoPath;
    }

    public void setFrontVideoPath(String frontVideoPath) {
        this.frontVideoPath = frontVideoPath;
    }

    public String getLeftVideoPath() {
        return leftVideoPath;
    }

    public void setLeftVideoPath(String leftVideoPath) {
        this.leftVideoPath = leftVideoPath;
    }

    public String getBottomVideoPath() {
        return bottomVideoPath;
    }

    public void setBottomVideoPath(String bottomVideoPath) {
        this.bottomVideoPath = bottomVideoPath;
    }

    public String getRightVideoPath() {
        return rightVideoPath;
    }

    public void setRightVideoPath(String rightVideoPath) {
        this.rightVideoPath = rightVideoPath;
    }

    public String getTopVideoPath() {
        return topVideoPath;
    }

    public void setTopVideoPath(String topVideoPath) {
        this.topVideoPath = topVideoPath;
    }
}
